package week1.day2;

public class IfelseResult {

	public static void main(String[] args) {
		int mark=50;
		if (mark>90)
		{
			System.out.println("The student secured A grade");
		} else if(mark>70)
		{
			System.out.println("The student secured B grade");
		}
		else if(mark>50)
		{	System.out.println("The student secured C grade");}
	else
	{
		System.out.println("The student secured D grade");
	}
}
}
